"""The Specialized Turbo integration."""

from __future__ import annotations

import logging

from homeassistant.config_entries import ConfigEntry
from homeassistant.const import CONF_ADDRESS, Platform
from homeassistant.core import HomeAssistant

from .const import CONF_PIN, DOMAIN
from .coordinator import SpecializedTurboCoordinator

_LOGGER = logging.getLogger(__name__)

PLATFORMS: list[Platform] = [Platform.SENSOR]

type SpecializedTurboConfigEntry = ConfigEntry


async def async_setup_entry(
    hass: HomeAssistant, entry: SpecializedTurboConfigEntry
) -> bool:
    """Set up Specialized Turbo from a config entry."""
    address: str = entry.data[CONF_ADDRESS]
    pin: int | None = entry.data.get(CONF_PIN)

    coordinator = SpecializedTurboCoordinator(
        hass,
        _LOGGER,
        address=address,
        pin=pin,
    )

    # Store coordinator so platforms can access it
    hass.data.setdefault(DOMAIN, {})
    hass.data[DOMAIN][entry.entry_id] = coordinator

    await hass.config_entries.async_forward_entry_setups(entry, PLATFORMS)

    # Start the coordinator â€” it will connect and subscribe on first poll
    entry.async_on_unload(coordinator.async_start)

    return True


async def async_unload_entry(
    hass: HomeAssistant, entry: SpecializedTurboConfigEntry
) -> bool:
    """Unload a Specialized Turbo config entry."""
    unload_ok = await hass.config_entries.async_unload_platforms(entry, PLATFORMS)

    if unload_ok:
        coordinator: SpecializedTurboCoordinator = hass.data[DOMAIN].pop(
            entry.entry_id
        )
        await coordinator.async_shutdown()

    return unload_ok
